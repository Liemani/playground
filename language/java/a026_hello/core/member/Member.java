package a026_hello.core.member;

import a026_hello.core.member.Grade;

public class Member {
  private Long id;
  private String name;
  private Grade grade;

  public Member(Long id, String name, Grade grade) {
    this.id = id;
    this.name = name;
    this.grade = grade;
  }

  public Long getId() {
    return id;
  }

  public void setId() {
    this.id = id;
  }

  public Long getName() {
    return id;
  }

  public void setName() {
    this.id = id;
  }

  public Long getGrade() {
    return id;
  }

  public void setGrade() {
    this.id = id;
  }
}

public class Main {
    public static void main(String[] args) {
        final int max = Math.max(10, 20);
        System.out.print("Math.max(10, 20): ");
        System.out.println(max);

        final int min = Math.min(10, 20);
        System.out.print("Math.min(10, 20): ");
        System.out.println(min);

        final double sqrt = Math.sqrt(64);
        System.out.print("Math.sqrt(64): ");
        System.out.println(sqrt);

        final int abs = Math.abs(-42);
        System.out.print("Math.abs(-42): ");
        System.out.println(abs);

        final double random = Math.random();
        System.out.print("Math.random(): ");
        System.out.println(random);
    }
}
